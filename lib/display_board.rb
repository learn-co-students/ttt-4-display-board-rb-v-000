# Define display_board that accepts a board and prints
# out the current state.



  def display_board(board)
     print " #{board[0]} "
     print "| #{board[1]} |"
     print " #{board[2]} \n"
     print "-----------\n"
     print " #{board[3]} "
     print "| #{board[4]} |"
     print " #{board[5]} \n"
     print "-----------\n"
     print " #{board[6]} "
     print "| #{board[7]} |"
     print " #{board[8]} \n"
  end
